def get_matrix(input: str) -> List[str]:
    matrix = []
    for line in input.split('\n'):
        matrix.append(line)
    return matrix

def rotate_matrix(matrix: List[str]) -> List[str]:
    new_matrix = []
    for line in matrix:
        for i, c in enumerate(line):
            if len(new_matrix) == len(matrix):
                new_matrix[i] += c
            else:
                new_matrix.append(c)
    return new_matrix

# going right and up
def get_diagonals(matrix: List[str]) -> List[str]:
    diagonals = []
    for i in range(len(matrix) + len(matrix[0])):
        diagonal = ""
        for x in range(len(matrix[0])):
            y = i - x
            if 0 <= y < len(matrix) and 0 <= x < len(matrix[0]):
                diagonal += matrix[y][x]
        if len(diagonal) > 0:
            diagonals.append(diagonal)
    return diagonals

# going right and up
def get_down_diagonals(matrix: List[str]) -> List[str]:
    diagonals = []
    for i in range(len(matrix) + len(matrix[0])):
        diagonal = ""
        for x in range(len(matrix[0])):
            y = i - len(matrix[0]) + x
            if 0 <= y < len(matrix) and 0 <= x < len(matrix[0]):
                diagonal += matrix[y][x]
        if len(diagonal) > 0:
            diagonals.append(diagonal)
    return diagonals

def day4(input: str) -> int:
    matrix = get_matrix(input)
    xmas_count = 0
    # horizontal + horizontal backwards
    for line in matrix:
        xmas_count += line.count('XMAS')
        xmas_count += line.count('SAMX')
    # vertical - up and down
    rotated = rotate_matrix(matrix)
    for line in rotated:
        xmas_count += line.count('XMAS')
        xmas_count += line.count('SAMX')
    # diagonals right and up
    diagonals = get_diagonals(matrix)
    for line in diagonals:
        xmas_count += line.count('XMAS')
        xmas_count += line.count('SAMX')
    # diagonals right and down
    diagonals_down = get_down_diagonals(matrix)
    for line in diagonals_down:
        xmas_count += line.count('XMAS')
        xmas_count += line.count('SAMX')
    return xmas_count

def check_xmas(matrix: List[str], x: int, y: int) -> int:
    str_1 = matrix[y-1][x-1] + matrix[y][x] + matrix[y+1][x+1]
    str_2 = matrix[y+1][x-1] + matrix[y][x] + matrix[y-1][x+1]
    if str_1 in ['MAS', 'SAM'] and str_2 in ['MAS', 'SAM']:
        return 1
    return 0

def day4_part2(input: str) -> int:
    matrix = get_matrix(input)
    xmas_count = 0
    for y in range(1, len(matrix) - 1):
        for x in range(1, len(matrix[0]) - 1):
            xmas_count += check_xmas(matrix, x, y)
    return xmas_count

